<script lang="ts">
import { Options, Vue } from "vue-class-component";

@Options({
  emits: ["emitCallback"],
  props: {
    msg: String,
    num: Number,
    isB: Boolean,
    funCallback: {
      type: Function,
      required: true,
    },
  },
})
export default class HelloWorld extends Vue {
  msg!: string;
  num!: number;
  isB!: boolean;

  mNum = 0;

  created() {
    this.mNum = this.num;
  }

  addNum() {
    this.mNum++;
    console.log("点击 mNum = ", this.mNum);
  }

  funCallback!: (n: number) => void;
  toFather(n: number) {
    this.funCallback(n);
  }

  emitCallback(n: number) {
    this.$emit("emitCallback", n);
  }
}
</script>

<template>
  <div class="hello">
    <h1>{{ "传递的文本： " + msg }}</h1>
